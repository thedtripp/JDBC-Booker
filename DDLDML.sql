-- create the table "Writing Groups"
CREATE TABLE WRITINGGROUPS (
    GroupName varchar(100),
    HeadWriter varchar(100),          
    YearFormed int,
    Subject varchar(100),
    PRIMARY KEY (GROUPNAME)
);

-- create the table "Publishers"
CREATE TABLE PUBLISHERS (
    PublisherName varchar(100),
    PublisherAddress varchar(100),
    PublisherPhone varchar(100),
    PublisherEmail varchar(100),
    PRIMARY KEY (PublisherName)
);

-- creating the table "books"
CREATE TABLE BOOKS (
    GroupName varchar(100),
    BookTitle varchar(100),
    PublisherName varchar(100),
    YearPublished int,
    NumberPages int,
    PRIMARY KEY (GROUPNAME, BOOKTITLE),
    FOREIGN KEY (GROUPNAME) references  WRITINGGROUPS(GROUPNAME),
    FOREIGN KEY (PublisherName) references PUBLISHERS(PublisherName)
);



